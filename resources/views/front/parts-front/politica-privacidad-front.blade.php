                    <p style="max-width: 80%">

                       Casillero Express, en cumplimiento de lo previsto en la Ley 1581 de 2012, por la cual se dictan disposiciones generales para la protección de datos personales, y el Decreto 1377 de 2013, pone a su disposición sus Políticas de Privacidad, con las cuales informa al titular de datos personales acerca de las políticas de tratamiento de información que han sido adoptadas por la compañía, la forma de acceder a éstas y las características del tratamiento que se da a los datos personales recabados.

                       <br><br>

                       Usted como persona natural podrá contar con el compromiso de Casillero Express respecto del Tratamiento que hace de los datos suministrados para adquirir los servicios ofrecidos por la empresa.

                       <br><br>

                       <b>1. UBICACIÓN</b>

                       <br><br>

                       Casillero Express está ubicado físicamente en la dirección 3509 nw 115 Ave, Doral FL 33178 USA. Además, la empresa tiene dispuestos como medios de contacto el siguiente correo electrónico: info@casilleroexpress.com, junto con la línea telefónica +1(305) 934-9442.

                       <br><br>

                       Para los fines asociados al servicio de la empresa, además de la dirección física en Miami, la empresa cuenta con ubicación física en la Avd. Carabanchel Alto 68. CP 28044, en la ciudad de Madrid España, y sede de operaciones en Bogotá, Colombia, ubicada en la Av. El Dorado # 103-09 Bodega 1 Edificio CISA CP 110911

                       <br><br>

                       <b>2. TRATAMIENTO DE LOS DATOS ADMINISTRADOS POR CASILLERO EXPRESS</b>

                       <br><br>

                       Casillero Express, en desarrollo de su objeto social, obtiene y administra datos de identificación, contacto, correspondencia y datos bancarios (referidos al medio de pago seleccionado), de sus clientes.

                       <br><br>

                       Así también, en desarrollo de su gestión administrativa, recaba, almacena, administra y conserva datos personales de empleados y proveedores. Esto queda establecido claramente en las políticas de tratamiento de datos y de privacidad, las cuales siempre estarán disponibles para su consulta en la página web www.casilleroexpressusa.com

                       <br><br>

                       <b>3. FINALIDADES DEL TRATAMIENTO DE DATOS PERSONALES</b>

                       <br><br>

                       La información personal de los clientes de Casillero Express es utilizada con el fin de: (i) personalizar el casillero asignado al titular en las ciudades de Miami o Madrid; (ii) enviar los paquetes recibidos en el casillero a la dirección de correspondencia del titular en Colombia, a través de empresas especializadas en manejo de paquetería y aduana; (iii) debitar de la tarjeta de crédito registrada por el titular los valores correspondientes al pago de cargos de acuerdo al peso y volumen de los paquetes recibidos y transportados a Colombia, así como el valor correspondiente al pago del Impuesto al Valor Agregado (IVA) y de los demás impuestos o aranceles aduaneros a los que haya lugar.

                       <br><br>

                       El tratamiento de la información de los trabajadores tiene como finalidad el desarrollo natural de las relaciones laborales que existan con ellos. El tratamiento de sus datos personales también podrá realizarse con el fin de proveerles alternativas o beneficios en aspectos que contribuyan a su bienestar y el de su grupo familiar, siempre que se cuente con autorización del empleado para tal efecto.

                       <br><br>

                       En el caso de los proveedores que sean personas naturales sus datos personales serán recabados para conocer y hacer seguimiento a la idoneidad y especialización para la prestación del bien o servicio contratado. Así mismo, se cuenta con la información de proveedores que sea requerida para efectuar los pagos a que haya lugar y para exigir el cumplimiento de las obligaciones contraídas.

                       <br><br>

                       <b>4. DERECHOS DE LOS TITULARES</b>

                       <br><br>

                       Como titular de datos personales usted tiene los siguientes derechos:

                       <br><br>

                       <i class="fas fa-caret-right"></i> Conocer, actualizar y rectificar sus datos personales y suprimirlos, cuando haya lugar a ello y no sean requeridos en atención a un deber legal o contractual vigente. Este derecho se podrá ejercer, entre otros, frente a datos parciales, inexactos, incompletos, fraccionados, que induzcan a error o aquellos cuyo tratamiento esté expresamente prohibido o no haya sido autorizado.

                       <br><br>

                        <i class="fas fa-caret-right"></i> Solicitar prueba de la autorización otorgada al responsable del tratamiento salvo cuando expresamente se exceptúe como requisito, de conformidad con lo previsto en el artículo 10 de la citada ley.

                        <br><br>

                        <i class="fas fa-caret-right"></i> Ser informado por el responsable o encargado del tratamiento, previa solicitud, respecto del uso que le ha dado a sus datos personales.

                        <br><br>

                        <i class="fas fa-caret-right"></i> Presentar ante la Superintendencia de Industria y Comercio quejas por infracciones a lo dispuesto en la presente ley y las demás normas que la modifiquen, adicionen o complementen.

                        <br><br>

                        <i class="fas fa-caret-right"></i> Acceder en forma gratuita, una vez al mes, a los datos personales que hayan sido objeto de tratamiento.

                        <br><br>

                        <b>5. TRÁMITE DE CONSULTAS Y RECLAMOS</b>

                        <br><br>

                        Casillero Express le informa que tanto para consultas como reclamos relacionados con la administración de sus datos personales, podrá dirigir una comunicación escrita al correo electrónico info@casilleroexpress.com, o a las instalaciones administrativas de la sociedad ubicadas en Bogotá, en la dirección Av. Calle 26 # 99j- 66, Edificio Optimus Oficina 303. Para ello, deberá acreditar que usted es el titular de los datos, adjuntando copia de su documento de identidad e indicando su dirección física o electrónica en la cual desea recibir la respuesta.

                        <br><br>

                        Cuando se trate de la formulación de reclamos deberá describir los hechos que dan lugar al mismo y los documentos que quiera hacer valer como prueba.

                        <br><br>

                        En el evento en que usted actúe como causahabiente del titular, representante o apoderado del mismo, deberá adjuntar el documento que acredite que tiene tal calidad, bien sea el registro civil de nacimiento o de defunción del titular, o el poder correspondiente cuando obre como apoderado.

                        <br><br>

                        En el caso en que falte alguno de los requisitos aquí establecidos, se le informará dentro de los cinco (5) días siguientes a la recepción de las solicitudes para que los complete, si no los subsana no se podrá dar trámite a su solicitud. En el caso de los reclamos, transcurridos dos (2) meses desde la fecha del requerimiento sin que sean subsanados se entenderá que ha desistido del mismo.

                        <br><br>

                        Los plazos para obtener sus respuestas en el caso de consultas es de diez (10) días hábiles contados a partir de la fecha de su recibo, éste término podrá ser prorrogado por cinco (5) días hábiles más. Por su parte los reclamos serán resueltos en quince (15) días hábiles, prorrogables por ocho (8) días hábiles más de conformidad con lo que dispone la Ley.

